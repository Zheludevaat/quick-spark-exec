/** Paint a pixel grid (rows of digits 0-3 or '.') onto a canvas. */
export function paintGrid(
  ctx: CanvasRenderingContext2D,
  rows: string[],
  palette: Palette4,
  ox = 0,
  oy = 0,
) {
  for (let y = 0; y < rows.length; y++) {
    const row = rows[y];
    for (let x = 0; x < row.length; x++) {
      const ch = row[x];
      if (ch === "." || ch === " ") continue;
      const idx = ch.charCodeAt(0) - 48; // '0'..'3'
      if (idx < 0 || idx > 3) continue;
      ctx.fillStyle = palette[idx];
      ctx.fillRect(ox + x, oy + y, 1, 1);
    }
  }
}

/** Helper: create a CanvasTexture and return its 2d ctx. */
export function makeTex(scene: Phaser.Scene, key: string, w: number, h: number) {
  if (scene.textures.exists(key)) scene.textures.remove(key);
  const tex = scene.textures.createCanvas(key, w, h)!;
  return { tex, ctx: tex.getContext() as CanvasRenderingContext2D };
}

/** Register a grid of numbered frames on a CanvasTexture (left-to-right, top-to-bottom). */
export function addGridFrames(
  tex: Phaser.Textures.CanvasTexture,
  frameW: number,
  frameH: number,
  cols: number,
  rows: number,
) {
  let i = 0;
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      tex.add(i, 0, c * frameW, r * frameH, frameW, frameH);
      i++;
    }
  }
}

