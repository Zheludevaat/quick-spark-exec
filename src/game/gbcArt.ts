const CHAR_W = 5, CHAR_H = 7, CHAR_GAP = 1;

/** Draw GBC-style bitmap text into a CanvasTexture, returning the texture key. */
export function drawBitmapText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color = COLOR.textLight,
  shadow?: string,
) {
  const upper = text.toUpperCase();
  let cx = x;
  for (const ch of upper) {
    const glyph = FONT[ch] || FONT["?"];
    if (shadow) {
      ctx.fillStyle = shadow;
      for (let gy = 0; gy < CHAR_H; gy++) {
        for (let gx = 0; gx < CHAR_W; gx++) {
          if (glyph[gy][gx] === "1") ctx.fillRect(cx + gx, y + gy + 1, 1, 1);
        }
      }
    }
    ctx.fillStyle = color;
    for (let gy = 0; gy < CHAR_H; gy++) {
      for (let gx = 0; gx < CHAR_W; gx++) {
        if (glyph[gy][gx] === "1") ctx.fillRect(cx + gx, y + gy, 1, 1);
      }
    }
    cx += CHAR_W + CHAR_GAP;
  }
  return cx;
}

export function measureText(text: string) {
  const upper = text.toUpperCase();
  return upper.length * (CHAR_W + CHAR_GAP) - CHAR_GAP;
}

/** Word-wrap text into lines that fit a max pixel width. */
export function wrapText(text: string, maxPx: number): string[] {
  const words = text.split(" ");
  const lines: string[] = [];
  let cur = "";
  for (const w of words) {
    const trial = cur ? cur + " " + w : w;
    if (measureText(trial) <= maxPx) cur = trial;
    else { if (cur) lines.push(cur); cur = w; }
  }
  if (cur) lines.push(cur);
  return lines;
}

/**
 * Phaser GameObject that renders bitmap text into its own canvas texture.
 * Use this everywhere instead of scene.add.text for true GBC look.
 */
export class GBCText {
  obj: Phaser.GameObjects.Image;
  private scene: Phaser.Scene;
  private key: string;
  private maxWidthPx: number;
  private color: string;
  private shadow?: string;

  constructor(
    scene: Phaser.Scene,
    x: number,
    y: number,
    text: string,
    opts: { color?: string; shadow?: string; maxWidthPx?: number; depth?: number; scrollFactor?: number } = {},
  ) {
    this.scene = scene;
    this.color = opts.color ?? COLOR.textLight;
    this.shadow = opts.shadow;
    this.maxWidthPx = opts.maxWidthPx ?? 200;
    this.key = `gbctext_${Math.random().toString(36).slice(2, 9)}`;
    const lines = wrapText(text, this.maxWidthPx);
    const w = Math.max(1, ...lines.map(l => measureText(l)));
    const h = Math.max(1, lines.length * (CHAR_H + 2) - 2);
    const { ctx, tex } = makeTex(scene, this.key, w + (this.shadow ? 1 : 0), h + (this.shadow ? 1 : 0));
    lines.forEach((line, i) => drawBitmapText(ctx, line, 0, i * (CHAR_H + 2), this.color, this.shadow));
    tex.refresh();
    this.obj = scene.add.image(x, y, this.key).setOrigin(0, 0);
    if (opts.depth !== undefined) this.obj.setDepth(opts.depth);
    if (opts.scrollFactor !== undefined) this.obj.setScrollFactor(opts.scrollFactor);
  }
  setText(text: string, color?: string) {
    if (color) this.color = color;
    if (this.scene.textures.exists(this.key)) this.scene.textures.remove(this.key);
    const lines = wrapText(text, this.maxWidthPx);
    const w = Math.max(1, ...lines.map(l => measureText(l)));
    const h = Math.max(1, lines.length * (CHAR_H + 2) - 2);
    const { ctx, tex } = makeTex(this.scene, this.key, w + (this.shadow ? 1 : 0), h + (this.shadow ? 1 : 0));
    lines.forEach((line, i) => drawBitmapText(ctx, line, 0, i * (CHAR_H + 2), this.color, this.shadow));
    tex.refresh();
    this.obj.setTexture(this.key);
  }
  setColor(color: string) { this.setText(this.lastText() ?? "", color); }
  setVisible(v: boolean) { this.obj.setVisible(v); }
  setPosition(x: number, y: number) { this.obj.setPosition(x, y); }
  destroy() {
    this.obj.destroy();
    if (this.scene.textures.exists(this.key)) this.scene.textures.remove(this.key);
  }
  private _lastText?: string;
  private lastText() { return this._lastText; }
}

