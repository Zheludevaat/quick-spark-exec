const CHAR_W = 5,
  CHAR_H = 7,
  CHAR_GAP = 1;

/** Draw GBC-style bitmap text into a CanvasTexture, returning the texture key. */
export function drawBitmapText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color = COLOR.textLight,
  shadow?: string,
) {
  const upper = text.toUpperCase();
  let cx = x;
  for (const ch of upper) {
    const glyph = FONT[ch] || FONT["?"];
    if (shadow) {
      ctx.fillStyle = shadow;
      for (let gy = 0; gy < CHAR_H; gy++) {
        for (let gx = 0; gx < CHAR_W; gx++) {
          if (glyph[gy][gx] === "1") ctx.fillRect(cx + gx, y + gy + 1, 1, 1);
        }
      }
    }
    ctx.fillStyle = color;
    for (let gy = 0; gy < CHAR_H; gy++) {
      for (let gx = 0; gx < CHAR_W; gx++) {
        if (glyph[gy][gx] === "1") ctx.fillRect(cx + gx, y + gy, 1, 1);
      }
    }
    cx += CHAR_W + CHAR_GAP;
  }
  return cx;
}

export function measureText(text: string) {
  const upper = text.toUpperCase();
  return upper.length * (CHAR_W + CHAR_GAP) - CHAR_GAP;
}

/** Standard line height in px for GBC bitmap text (CHAR_H 7 + 2 leading). */
export const GBC_LINE_H = 9;

/** Pixel height of `text` after wrapping into `maxPx`. */
export function textHeightPx(text: string, maxPx: number): number {
  const lines = wrapText(text, maxPx);
  return Math.max(1, lines.length) * GBC_LINE_H - 2;
}

/**
 * Force `text` onto a single line that fits `maxPx`, appending literal "..."
 * when truncated. Always returns the uppercased form (GBC fonts are caps-only).
 */
export function fitSingleLineText(text: string, maxPx: number): string {
  const upper = text.toUpperCase();
  if (measureText(upper) <= maxPx) return upper;
  const suffix = "...";
  let out = upper;
  while (out.length > 0 && measureText(out + suffix) > maxPx) {
    out = out.slice(0, -1);
  }
  return out.length > 0 ? out + suffix : suffix;
}

/**
 * Returns true if `text` (uppercased) does not fit on a single line of `maxPx`.
 * Caller should surface the full text in a wrapped readout area when true.
 */
export function isSingleLineTrimmed(text: string, maxPx: number): boolean {
  return measureText(text.toUpperCase()) > maxPx;
}

/**
 * Compute both the compact one-line form and the full uppercased form of a
 * label, plus a flag that says whether trimming actually occurred. UIs use
 * `fitted` for stable menu rows and `full` for a wrapped readout area shown
 * whenever any row in the same UI was trimmed.
 */
export function fitSingleLineState(
  text: string,
  maxPx: number,
): { full: string; fitted: string; trimmed: boolean } {
  const full = text.toUpperCase();
  const trimmed = measureText(full) > maxPx;
  return {
    full,
    fitted: trimmed ? fitSingleLineText(full, maxPx) : full,
    trimmed,
  };
}

/** Word-wrap text into lines that fit a max pixel width. */
export function wrapText(text: string, maxPx: number): string[] {
  const words = text.split(" ");
  const lines: string[] = [];
  let cur = "";
  for (const w of words) {
    const trial = cur ? cur + " " + w : w;
    if (measureText(trial) <= maxPx) cur = trial;
    else {
      if (cur) lines.push(cur);
      cur = w;
    }
  }
  if (cur) lines.push(cur);
  return lines;
}

/**
 * Phaser GameObject that renders bitmap text into its own canvas texture.
 * Use this everywhere instead of scene.add.text for true GBC look.
 */
export class GBCText {
  obj: Phaser.GameObjects.Image;
  private scene: Phaser.Scene;
  private key: string;
  private maxWidthPx: number;
  private color: string;
  private shadow?: string;

  constructor(
    scene: Phaser.Scene,
    x: number,
    y: number,
    text: string,
    opts: {
      color?: string;
      shadow?: string;
      maxWidthPx?: number;
      depth?: number;
      scrollFactor?: number;
    } = {},
  ) {
    this.scene = scene;
    this.color = opts.color ?? COLOR.textLight;
    // ART UPGRADE: default 1px black drop-shadow so text always pops on textured frames.
    this.shadow = opts.shadow ?? "#000000";
    this.maxWidthPx = opts.maxWidthPx ?? 200;
    this.key = `gbctext_${Math.random().toString(36).slice(2, 9)}`;
    this._lastText = text;
    const lines = wrapText(text, this.maxWidthPx);
    const w = Math.max(1, ...lines.map((l) => measureText(l)));
    const h = Math.max(1, lines.length * (CHAR_H + 2) - 2);
    const { ctx, tex } = makeTex(
      scene,
      this.key,
      w + (this.shadow ? 1 : 0),
      h + (this.shadow ? 1 : 0),
    );
    lines.forEach((line, i) =>
      drawBitmapText(ctx, line, 0, i * (CHAR_H + 2), this.color, this.shadow),
    );
    tex.refresh();
    this.obj = scene.add.image(x, y, this.key).setOrigin(0, 0);
    if (opts.depth !== undefined) this.obj.setDepth(opts.depth);
    if (opts.scrollFactor !== undefined) this.obj.setScrollFactor(opts.scrollFactor);
  }
  setText(text: string, color?: string) {
    if (color) this.color = color;
    this._lastText = text;
    if (this.scene.textures.exists(this.key)) this.scene.textures.remove(this.key);
    const lines = wrapText(text, this.maxWidthPx);
    const w = Math.max(1, ...lines.map((l) => measureText(l)));
    const h = Math.max(1, lines.length * (CHAR_H + 2) - 2);
    const { ctx, tex } = makeTex(
      this.scene,
      this.key,
      w + (this.shadow ? 1 : 0),
      h + (this.shadow ? 1 : 0),
    );
    lines.forEach((line, i) =>
      drawBitmapText(ctx, line, 0, i * (CHAR_H + 2), this.color, this.shadow),
    );
    tex.refresh();
    this.obj.setTexture(this.key);
  }
  setColor(color: string) {
    this.setText(this._lastText ?? "", color);
  }
  setVisible(v: boolean) {
    this.obj.setVisible(v);
  }
  setPosition(x: number, y: number) {
    this.obj.setPosition(x, y);
  }
  destroy() {
    this.obj.destroy();
    if (this.scene.textures.exists(this.key)) this.scene.textures.remove(this.key);
  }
  private _lastText?: string;
}

