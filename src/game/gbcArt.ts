  fill("............3333333333333333............." + ".....");
  // Neck → robe
  fill("..............33333333333333............." + ".....");
  fill(".............333333333333333.............." + "....");
  fill("............13333333333333331............." + "....");
  fill("...........122222222222222221............." + "....");
  fill("..........12222223232322222221............" + "....");
  fill("..........12222232323232322221............" + "....");
  fill("..........12222323232323232221............" + "....");
