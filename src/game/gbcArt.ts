/** Bake all tiles into one horizontal strip texture suitable for Phaser tilemaps. */
export function bakeTileset(scene: Phaser.Scene, key = "gbc_tiles") {
  const w = TILE * TILE_DEFS.length;
  const h = TILE;
  const { ctx, tex } = makeTex(scene, key, w, h);
  TILE_DEFS.forEach((t, i) => paintGrid(ctx, t.rows, PAL[t.pal], i * TILE, 0));
  addGridFrames(tex, TILE, TILE, TILE_DEFS.length, 1);
  tex.refresh();
  return key;
}

