/**
 * Run a Pokemon-style screen wipe. Plays out (cover) then calls onMid, then
 * plays in (reveal). Use for scene changes.
 */
export function gbcWipe(scene: Phaser.Scene, onMid: () => void, durMs = 280) {
  const bands = 8;
  const bandH = Math.ceil(GBC_H / bands);
  const rects: Phaser.GameObjects.Rectangle[] = [];
  for (let i = 0; i < bands; i++) {
    const r = scene.add
      .rectangle(0, i * bandH, 0, bandH, 0x0a0e1a, 1)
      .setOrigin(0, 0)
      .setScrollFactor(0)
      .setDepth(9999);
    rects.push(r);
  }
  const step = durMs / bands;
  rects.forEach((r, i) => {
    scene.tweens.add({
      targets: r,
      width: GBC_W,
      duration: durMs * 0.6,
      delay: i * (step * 0.4),
      ease: "Sine.in",
    });
  });
  scene.time.delayedCall(durMs, () => {
    onMid();
    scene.time.delayedCall(60, () => {
      rects.forEach((r, i) => {
        scene.tweens.add({
          targets: r,
          width: 0,
          duration: durMs * 0.6,
          delay: (bands - 1 - i) * (step * 0.4),
          ease: "Sine.out",
          onComplete: () => r.destroy(),
        });
      });
    });
  });
}

